export function searchResults(query: string, visual = false): Result[] {
  if (visual) return allResults;
  if (!query.trim()) return allResults;
  const tokens = query.toLowerCase().split(/\s+/);
  const scored = allResults.map((r) => {
    const score = tokens.reduce((acc, token) => {
      const tagMatch = r.tags.filter((t) => t.includes(token)).length;
      const titleMatch = r.title.toLowerCase().includes(token) ? 3 : 0;
      const descMatch = r.description.toLowerCase().includes(token) ? 1 : 0;
      return acc + tagMatch + titleMatch + descMatch;
    }, 0);
    return { result: r, score };
  });
  const matched = scored.filter((s) => s.score > 0).sort((a, b) => b.score - a.score);
  return matched.length > 0 ? matched.map((s) => s.result) : allResults;
}
